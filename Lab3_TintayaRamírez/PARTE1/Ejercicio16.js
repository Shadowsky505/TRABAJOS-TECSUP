let numero = 1;

let numerTexto = "Valor desconocido";

switch (numero) {
    case 1:
        numerTexto = "Número uno";
        break;
    case 2:
        numerTexto = "Número dos";
        break;
    case 3:
        numerTexto = "Número tres";
        break;
    case 4:
        numerTexto = "Número cuatro";
        break;
    default:
        numerTexto = "Valor desconocido";
        break;
}

console.log(numerTexto);